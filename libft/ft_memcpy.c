#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t	i;

	if (!dst && !src)
		return (0);
	i = 0;
	while (i < n)
	{
		*((char *)dst + (i / sizeof(char))) =
			*((char *)src + (i / sizeof(char)));
		++i;
	}
	return (dst);
}
